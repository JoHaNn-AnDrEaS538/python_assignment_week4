#module for strings
def reverse_string(str1):
    reverse = str1[::-1]
    print(reverse)

def capitalize_string(str1):
    caps=str1.capitalize()
    print(caps)

def lowercase_string(str1):
    lower=str1.lower()
    print(lower)

def uppercase_string(str1):
    upper=str1.upper()
    print(upper)